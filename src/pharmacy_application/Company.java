    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        String Id=cid.getText();
        String Name=cname.getText();
        String Address=caddress.getText();
        String Number=phonenumber.getText();
        String Email=emailid.getText();
        String Exp=experience.getText();
        if(isNumber(Number)){
          JOptionPane.showMessageDialog(this,"This Number Already Exits.....");
          return;
        }
        if(isEmail(Email)){
          JOptionPane.showMessageDialog(this,"This Email Id Already Exits.....");
          return;
        }
        if(!Id.isEmpty() && !Name.isEmpty() && !Address.isEmpty() && !Number.isEmpty() && !Email.isEmpty() && !Exp.isEmpty()){
          try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("sp_company_insert ?,?,?,?,?,?")){
               pst.setString(1,Id);
               pst.setString(2,Name);
               pst.setString(3,Address);
               pst.setString(4,Number);
               pst.setString(5,Email);
               pst.setString(6,Exp);
               int a=pst.executeUpdate();
               if(a>0){
                  JOptionPane.showMessageDialog(this,"Company Details Added...");
                  tab();
                  clear();
                  increment();
              }else{JOptionPane.showMessageDialog(this,"Failed Company Details Add Process...");}
          }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
        }else{JOptionPane.showMessageDialog(this,"Please Enter All The Fields.....");}
    }//GEN-LAST:event_addActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
         String Id=cid.getText();
        String Name=cname.getText();
        String Address=caddress.getText();
        String Number=phonenumber.getText();
        String Email=emailid.getText();
        String Exp=experience.getText();
         if(!isNumberOwner(Id,Number)&& isNumber(Number)){
              JOptionPane.showMessageDialog(this,"This Number Already Exits. Please Enter Another Phone Number");
              return;
           }
           if(!isEmailOwner(Id,Email)&& isEmail(Email)){
              JOptionPane.showMessageDialog(this,"This Email Id Already Exits. Please Enter Another Email Id");
              return;
           }
          if(!Id.isEmpty()){
              try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("sp_company_update ?,?,?,?,?,?")){
                pst.setString(1,Id);
               pst.setString(2,Name);
               pst.setString(3,Address);
               pst.setString(4,Number);
               pst.setString(5,Email);
               pst.setString(6,Exp);
               int a=pst.executeUpdate();
               if(a>0){
                  JOptionPane.showMessageDialog(this,"Company Details Edited...");
                  tab();
                  clear();
                  increment();
              }else{JOptionPane.showMessageDialog(this,"Failed Company Details Edit Process...");}
              }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
          }else{JOptionPane.showMessageDialog(this,"Please Select Company Id");}
    }//GEN-LAST:event_editActionPerformed

