     public void increment(){
       try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("select max(company_id) from company")){
           ResultSet rs=pst.executeQuery();
           rs.next();
           String Mid=rs.getString(1);
            if(Mid==null){
             cid.setText("CP001");
            }
            else{
            int id=Integer.parseInt(Mid.substring(2));
            id++;
            cid.setText(String.format("CP%03d",id));
            }
       }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
    }
     public void tab(){
       try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("sp_company_getall")){
           ResultSet rs=pst.executeQuery();
           DefaultTableModel m=(DefaultTableModel)table.getModel();
           m.setRowCount(0);
           while(rs.next()){
             m.addRow(new Object[]{rs.getString("company_id"),rs.getString("companyname"),rs.getString("address"),
             rs.getString("phonenumber"),rs.getString("email"),rs.getString("experience")});
           }
       }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
     }
