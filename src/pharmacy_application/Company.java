    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        String Id=cid.getText();
        int a=JOptionPane.showConfirmDialog(this,"DO YOU WANT DELETE THIS COMPANY ?","SELECT",JOptionPane.YES_NO_OPTION);
        if(!Id.isEmpty()){
            if(a==0){
               try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("sp_company_delete ?")){
                   pst.setString(1,Id);
                   pst.executeUpdate();
                   JOptionPane.showMessageDialog(this,"This Company Is Deleted...");
                   tab();
                   clear();
                   increment();
               }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
            }            
        }else{JOptionPane.showMessageDialog(this,"Please Select Company Id");}
    }//GEN-LAST:event_deleteActionPerformed

