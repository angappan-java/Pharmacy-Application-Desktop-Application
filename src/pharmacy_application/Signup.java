  public void increment(){
       try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("select max(id) from login")){
           ResultSet rs=pst.executeQuery();
           rs.next();
           String Mid=rs.getString(1);
            if(Mid==null){
             id.setText("AG001");
            }
            else{
            int Cid=Integer.parseInt(Mid.substring(2));
            Cid++;
            id.setText(String.format("AG%03d",Cid));
            }
       
           
       }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
    }
