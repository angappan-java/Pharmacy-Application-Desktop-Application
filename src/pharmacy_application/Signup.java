    private void signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupActionPerformed
        // TODO add your handling code here:
        String Id=id.getText();
        String Name=name.getText();
        String Password=password.getText();
        String Qus=(String)sque.getText();
        String Ans=sans.getText();
        if(!Id.isEmpty() && !Name.isEmpty() && !Password.isEmpty() && !Qus.isEmpty() && !Ans.isEmpty()){
            try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("sp_login_insert ?,?,?,?,?")){
                pst.setString(1,Id);
                pst.setString(2,Name);
                pst.setString(3,Password);
                pst.setString(4,Qus);
                pst.setString(5,Ans);
                int a=pst.executeUpdate();
                if(a>0){JOptionPane.showMessageDialog(this,"REGISTER SUCCESFULLY......");
                }else{JOptionPane.showMessageDialog(this,"Failed register Process...");}
            }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
        }else{JOptionPane.showMessageDialog(this,"Please Enter All Fields...");}
    }//GEN-LAST:event_signupActionPerformed

