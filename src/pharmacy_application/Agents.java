    private boolean isNumber(String Number){
          try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("select count(*) from agents where phonenumber=?")){
              pst.setString(1,Number);
              ResultSet rs=pst.executeQuery();
              if(rs.next()){
                 return rs.getInt(1)>0;
              }
          }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
          return false;
    }
    private boolean isNumberOwner(String Id,String Number){
          try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("select count(*) from agents where id=? and phonenumber=?")){
              pst.setString(1,Id);
              pst.setString(2,Number);
              ResultSet rs=pst.executeQuery();
              if(rs.next()){
                 return rs.getInt(1)>0;
              }
          }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
          return false;
    }
