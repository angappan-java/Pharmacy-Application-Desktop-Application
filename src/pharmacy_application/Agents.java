    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        String Id=(String)id.getSelectedItem();
        String Name=name.getText();
        String Password=password.getText();
        int Age=Integer.parseInt(age.getText());
        String Gender=(String)gender.getSelectedItem();
        String Address=address.getText();
        String Number=phonenumber.getText();
        String Email=emailid.getText();
        String Text="SELECT GENDER";
        if(Gender.equals(Text)){
          JOptionPane.showMessageDialog(this,"Please Select The Gender...");
          return;
        }
         if(isNumber(Number)){
              JOptionPane.showMessageDialog(this,"This Number Already Exits. Please Enter Another Phone Number");
              return;
           }
           if(isEmail(Email)){
              JOptionPane.showMessageDialog(this,"This Email Id Already Exits. Please Enter Another Email Id");
              return;
           }
        if(!Id.isEmpty() && !Name.isEmpty() && !Password.isEmpty() && !Address.isEmpty() && !Number.isEmpty() && !Email.isEmpty())
        {
          try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("sp_agents_insert ?,?,?,?,?,?,?,?")){
               pst.setString(1,Id);
               pst.setString(2,Name);
               pst.setString(3,Password);
               pst.setInt(4,Age);
               pst.setString(5,Gender);
               pst.setString(6,Address);
               pst.setString(7,Number);
               pst.setString(8,Email);
               int a=pst.executeUpdate();
              if(a>0){
                   
                 JOptionPane.showMessageDialog(this,"Agents Details Added.....");
                 clear();
                 tab();
                 
                }else{JOptionPane.showMessageDialog(this,"Failed For Agents Details Add Process..");}
           }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
        }else{JOptionPane.showMessageDialog(this,"Please Enter All The Fields....");}
    }//GEN-LAST:event_addActionPerformed

