    public void tab(){
       try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("sp_agents_getall")){
         ResultSet rs=pst.executeQuery();
         DefaultTableModel m=(DefaultTableModel)table.getModel();
         m.setRowCount(0);
         while(rs.next()){
            m.addRow(new Object[]{rs.getString("id"),rs.getString("name"),rs.getString("password"),rs.getInt("age"),rs.getString("gender"),rs.getString("address"),
                     rs.getString("phonenumber"),rs.getString("email")
            });
         }
       }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
     }
  
