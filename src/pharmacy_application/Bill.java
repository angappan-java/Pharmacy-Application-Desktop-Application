    private void billActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_billActionPerformed
        // TODO add your handling code here:
        String BillNo=bid.getText();
        String Total=total.getText();
        String Paid=paid.getText();
        String Balance=balance.getText();
        if(!Total.isEmpty() && !Paid.isEmpty() && !Balance.isEmpty()){
            try(Connection c=JDBC.con();PreparedStatement ps=c.prepareStatement("sp_bill_insert ?,?,?,?,?,?,?,?,?")){
                 for(int i=0;i<billtable.getRowCount();i++){
                     ps.setString(1,BillNo);
                     ps.setString(2,billtable.getValueAt(i,1).toString());
                     ps.setString(3,billtable.getValueAt(i,2).toString());
                     ps.setString(4,billtable.getValueAt(i,3).toString());
                     ps.setString(5,Total);
                     ps.setString(6,Paid);
                     ps.setString(7,Balance);
                     ps.setString(8,date.getText());
                     ps.setString(9,time.getText());
                     ps.addBatch();
                 }
                 ps.executeBatch();
                 JOptionPane.showMessageDialog(this,"Bill Is Saved For Store Area.....");
             }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
        }else{JOptionPane.showMessageDialog(this,"Please Paid For Bill");}       
    }//GEN-LAST:event_billActionPerformed

