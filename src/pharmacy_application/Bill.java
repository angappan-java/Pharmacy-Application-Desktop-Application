    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        // TODO add your handling code here:
        DefaultTableModel m=(DefaultTableModel)billtable.getModel();
        int index=billtable.getSelectedRow();
        if(index==-1){
           JOptionPane.showMessageDialog(this,"Please Select The Row Bill table For Removed  Medicine In Bill..");
           return;
       }
        if(billtable.getSelectedRowCount()==1){
                String MedName=billtable.getValueAt(index,1).toString();
                int Quan=Integer.parseInt(billtable.getValueAt(index,2).toString());
                m.removeRow(index);
          try(Connection c=JDBC.con();PreparedStatement pst=c.prepareStatement("sp_medicine_add ?,?");
          PreparedStatement st=c.prepareStatement("select * from medicine where name=?")){
                pst.setString(1,MedName);
                pst.setInt(2,Quan);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this,"THIS MEDICIENE REMOVE FROM BILL TABLE.....");
                pst.close();
              
                finaltotal=finaltotal-tot1;
                String T=String.valueOf(finaltotal);
                total.setText(T);
                total.setEditable(false);
               
                st.setString(1,MedName);
                ResultSet rs=st.executeQuery();
                if(rs.next())
                {
                  mname.setText(rs.getString("name"));
                  stock.setText(rs.getString("quantity"));
                  price.setText((rs.getString("price")));
                }
                quan.setText("");
            }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
        }
    }//GEN-LAST:event_removeActionPerformed

