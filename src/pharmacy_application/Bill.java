        // TODO add your handling code here:
        String Name=mname.getText();
        if(!Name.isEmpty()){
            try(Connection c=JDBC.con();PreparedStatement st=c.prepareStatement("select * from medicine where name=?")){
                st.setString(1,Name);
                ResultSet rs=st.executeQuery();
                if(rs.next()){
                   String Stock=rs.getString("quantity");
                   String Price=rs.getString("price");
                   stock.setText(Stock);
                   price.setText(Price);
                }
            }catch(Exception e){JOptionPane.showMessageDialog(this,e);}
        }else{JOptionPane.showMessageDialog(this,"Please Enter Medicine Name");}
